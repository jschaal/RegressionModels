w <- c(2, 1, 3, 1)
le <- function(x,w,u) {
    for (j in 1:length(u))
    {
        s <- 0
        for (i in 1:length(x))
        {
            s <- s+ w[i] * (x[i]-u[j])^2
        }
        print(paste(u[j],s))
    }
}
a <- c(.0025,.3,.1471,1.077)
le(x,w,a)

